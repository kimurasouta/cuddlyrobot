package java課題25;/*処理*/

public class Theme3 {

	private int hp, mp, attack, speed, defense;
	
	// HPを設定するメソッド
	public void setHp(int hp) {
		this.hp = hp;
	}
	
	// HPを取得するメソッド
	public int getHp() {
		return this.hp;
	}
	
	// MPを設定するメソッド
	public void setMp(int mp) {
		this.mp = mp;
	}
	
	// MPを取得するメソッド
	public int getMp() {
		return this.mp;
	}
	
	// attackを設定する
	public void setAttack(int attack) {
		this.attack = attack;
	}
	
	// attackを取得する
	public int getattack() {
		return this.attack;
	}

	// speedを設定する
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	
	// speedを取得する
	public int getspeed() {
		return this.speed;
	}

	// defenseを設定する
	public void setDefense(int defense) {
		this.defense = defense;
	}

	//defenseを取得する
	public int getdefense() {
		return this.defense;
	}

}
